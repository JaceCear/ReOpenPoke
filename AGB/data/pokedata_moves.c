/**Get the amount of attacks in species' moveset */
int GetAmountOfMoves(int species)
{
	const aLearnedMove* MoveSet = GetRef_PokemonMoveset(species);

	if(species < pkTotal)
	{
		int counter = 0;
		while(1)
		{
			// If the loop reached the end of the MoveSet,
			// return counter-1 (the amount of moves in the MoveSet)
			if(	MoveSet[counter].Level == 0x7F &&
				MoveSet[counter].Move == 0x1FF)
			{
				return	counter;
			}
			// If the move is valid, increase the counter
			else
			{
				counter++;
			}
		}
	}

	return 0;
}

// Returns a reference to \a speciesIndex's MoveSet
	// if speciesIndex is invalid:
		// return 0
// MissingNo.'s moveset will only be returned,
// if speciesIndex > pkTotal
#ifndef SIZEOF_MOVES_LUT
#define SIZEOF_MOVES_LUT ( sizeof(LearnedMoves) / sizeof(aLearnedMove) )
const aLearnedMove *GetRef_PokemonMoveset(int speciesIndex)
{
	aLearnedMove* MoveSet;

	if(speciesIndex < pkTotal) // NOTE: pkTotal is defined at "data/pokedata.h"
	{
		// Used to check every single attack
		int ScrollCounter = 0;
		// Used as moveset-index
		int pokeIndex = 0;

		// Scroll through attacks;
			// increase pokeIndex when ENDOF_MOVESET is found
		while(ScrollCounter < SIZEOF_MOVES_LUT)
		{
			MoveSet = &LearnedMoves[ScrollCounter++];
			if(	MoveSet->Level == 0x7F &&
				MoveSet->Move == 0x1FF &&
				++pokeIndex == speciesIndex)
			{
				return &LearnedMoves[ScrollCounter];
			}
		}
	}

	return &LearnedMoves[0];
}
#undef SIZEOF_MOVES_LUT
#endif //SIZEOF_MOVES_LUT

#undef ENDOF_MOVESET
#endif // #ifdef ENDOF_MOVESET
