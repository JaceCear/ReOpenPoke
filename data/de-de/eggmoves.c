/*
  BULBAPEDIA

  Egg moves are moves passed down from a Pokémon breeding pair to their offspring. Egg moves are passed down in the following ways:

  1 - If both parents know one or more moves that their offspring could learn by leveling up, the offspring will have that move.
  2 - If the male Pokémon knows a TM or HM move that the offspring can learn, the offspring will have that move.
  3 - If the male Pokémon knows a move that the offspring can learn only by breeding. For example, if a male Dragonite knowing Outrage is bred with a female Charizard, the resulting Charmander will know Outrage.

  All offspring Pokémon, in the absence of passed-down moves, will know at least one move when hatched, labeled in their level up move list as either "Start" or "--" in lieu of a level number. If these moves, together with the other egg moves obtained in the above ways, number more than four, then the moves on top of their level up move list (with "Start" or "--") will be the ones that do not show up.

  Exceptions
	 *  Beldum cannot inherit any Egg Moves and starts with only Take Down.
	 *  Pichu can "inherit" Volt Tackle if the female Pikachu of the breeding pair holds the Light Ball.


	KAWA's NOTES

	Rule 3 is not covered by this list. Strawhat's pokédex lists Outrage under "Egg Moves" for Charizard, but this list covers only Basic forms.
	Exception 1 is confirmed - Beldum is not in this list, and has no items in Strawhat's pokédex. Therefore, Rule 4 takes effect to give Beldum Take Down.
	Exception 2 is confirmed - Volt Tackle is not in the list and is apparently a hardcoded effect.

	WHAT WE CAN DO

	Rules 1 and 2. Because of Note 1.

*/

